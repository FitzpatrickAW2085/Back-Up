number1 = input("First number: ")
number2 = input("\nSecond number: ")

sum = float(number1) + float(number2)
print("{0} + {1} = {2}" .format(number1, number2, sum)) 

sum = float(number1) - float(number2)
print("{0} - {1} = {2}" .format(number1, number2, sum)) 

sum = float(number1) * float(number2)
print(" {0} * {1} = {2}" .format(number1, number2, sum)) 

sum = float(number1) / float(number2)
print("{0} / {1} = {2}" .format(number1, number2, sum)) 